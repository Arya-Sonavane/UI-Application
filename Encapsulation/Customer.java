class Customer
{
  private int id;
  private String name;
  private String review;

  public void setId(int id)
  {
    this.name=name;
  }
  public int getId()
  {
    return id;
  }
  public void setName(String name)
  {
    this.name=name;
  }
  public String getName()
  {
    return name;
  }
  public void setReview(String review)
  {
    this.review=review;
  }
  public String getReview()
  {
    return review;
  }
 
  
 

}